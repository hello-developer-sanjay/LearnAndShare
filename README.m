<h1>HogwartsEdX - Where Magic Meets Technology for Next-Level Learning</h1>

<p>Welcome to <strong>HogwartsEdX</strong>, a magical platform where technology and learning converge to create an enchanting educational experience. Whether you're a wizard, witch, or Muggle, our platform offers a seamless and captivating journey through the realms of knowledge and coding.</p>

<h2>Homepage</h2>

<p>The journey begins at our <a href="https://hogwartsedx.vercel.app/">Homepage</a>, where a simple click on the <strong>"Get Started"</strong> button will transport you to the <a href="https://hogwartsedx.vercel.app/explore">PostPage</a>. Here, you can explore a myriad of blog titles, each promising a wealth of knowledge. Utilize the search and navigation functionalities to delve into topics that intrigue you.</p>

<h2>PostPage</h2>

<p>On the <a href="https://hogwartsedx.vercel.app/explore">PostPage</a>, discover a collection of informative blog titles. Click on any title to navigate to the detailed blog post. The search functionality allows you to find specific topics of interest quickly and efficiently.</p>

<h2>Category Page</h2>

<p>Our <a href="https://hogwartsedx.vercel.app/category">Category Page</a> features 20 diverse course categories. Follow your favorite categories to stay updated. When you follow a category, you'll receive timely notifications about updates directly to your registered email. A message, <strong>"You have successfully followed the category! Now you will receive timely notifications about updates in this category directly to your registered email,"</strong> confirms your subscription.</p>

<h2>Add-Post Page</h2>

<p>Become a contributor on the <a href="https://hogwartsedx.vercel.app/add-post">Add-Post Page</a>. Fill out the form to add your blog post, but remember to accept our privacy policy first. Users must be logged in to submit a blog post, ensuring the safety and authenticity of our content.</p>

<h2>BlogPost Completion Certificates</h2>

<p>Dedication pays off at HogwartsEdX! If you mark all posts within a particular category as completed, you'll be awarded a completion certificate from HogwartsEdX, signed by our founder, <strong>Sanjay Patidar</strong>. Each certificate is unique, featuring a unique ID that allows for verification.</p>

<h2>Verify Certificate</h2>

<p>Verify your certificates on our <a href="https://hogwartsedx.vercel.app/certificate-verification">Certificate Verification Page</a>. Enter the username, date of completion, or unique ID to display certificate details and download the certificate.</p>

<h2>Code Editor</h2>

<p>Unleash your coding prowess with our <a href="https://hogwartsedx.vercel.app/editor">Code Editor</a>. This feature-rich tool allows users to add multiple windows, write code, and enjoy functionalities such as autosave, multiple themes, syntax highlighting, and download options (as text or JPEG). Your code remains intact even if you close the tab, thanks to our autosave functionality.</p>

